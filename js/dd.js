$(function() {

  $(".shopd").mouseover(function(){
    $(".shopdd").css("display","block");
  });
  $(".shopdd").mouseover(function(){
    $(".shopdd").css("display","block");
  });


  $(".shopd").mouseout(function(){
    $(".shopdd").css("display", "none");
  });
  $(".shopdd").mouseout(function(){
    $(".shopdd").css("display","none");
  });

});

